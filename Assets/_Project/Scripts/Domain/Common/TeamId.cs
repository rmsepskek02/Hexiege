// ============================================================================
// TeamId.cs
// 게임 내 진영(팀)을 구분하는 열거형.
// 타일 소유권, 유닛 소속 등 게임 전반에서 "누구 편인가"를 나타낼 때 사용.
//
// 사용 예시:
//   tile.Owner = TeamId.Blue;       // 이 타일은 블루 팀 소유
//   unit.Team == TeamId.Red         // 이 유닛이 레드 팀인지 확인
//
// Presentation 레이어에서 팀별 색상 매핑:
//   Neutral → 회색 RGB(178,178,178)
//   Blue    → 파랑 RGB(77,128,230)
//   Red     → 빨강 RGB(230,77,77)
// ============================================================================

namespace Hexiege.Domain
{
    public enum TeamId
    {
        Neutral = 0,  // 중립 (아무도 점령하지 않은 타일)
        Blue = 1,     // 파랑 진영 (플레이어 또는 하단)
        Red = 2       // 빨강 진영 (상대방 또는 상단)
    }
}
